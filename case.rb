today_weather = 'rain'

case today_weather
when 'rain'
puts "Get your umbrella, rain coat, and boots."
when 'snow'
puts "Boots, gloves, hats, scarf, and winter jacket."
when 'ice'
puts "Boots, scarf, gloves, and winter jacket."
when 'cold'
puts "Light jacket and a scarf."
when 'sunny'
puts "Sunglasses, swim suit, and sun tan lotion."
when 'windy'
puts "No hats. Stick with a light jacket if you get a cold easily."
else
puts "Hmmmm, I don't know what to tell you."
end

